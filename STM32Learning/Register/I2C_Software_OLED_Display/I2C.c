#include "I2C.h"


/***************************************************************************
* @ModuleName: void I2C_Start(void) 
* @param: 无
* @brief：无
* @retval: 无
****************************************************************************/

void I2C_Start()
{
	I2C_SDA = 1 ;
	I2C_SCL = 1 ;
	I2C_SDA = 0 ;
	I2C_SCL = 0 ;
}

/***************************************************************************
* @ModuleName: void I2C_Stop(void)
* @param: 无
* @brief：无
* @retval: 无
****************************************************************************/
void I2C_Stop(void)
{
	I2C_SDA = 0 ;
	I2C_SCL = 1 ;
	I2C_SDA = 1 ;
}